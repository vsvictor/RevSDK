public enum Protocol {
    STANDART,
    QUIC,
    REV;

    @Override
    public String toString() {
        switch (this){
            case STANDART: return "standard";
            case QUIC: return "quic";
            case REV: return "rmp";
        }
        return super.toString();
    }
}

